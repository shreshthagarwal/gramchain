export default function ConnectAccounts() {
  const router = useRouter();
  const [connectedAccounts, setConnectedAccounts] = useState<string[]>([]);
  const [error, setError] = useState('');

  const handleConnect = async (platformId: string) => {
    try {
      // TODO: Implement actual OAuth flow
      setConnectedAccounts([...connectedAccounts, platformId]);
    } catch (err) {
      setError('Failed to connect account. Please try again.');
    }
  };

  const handleFinish = async () => {
    try {
      // TODO: Implement final onboarding step
      const response = await fetch('/api/onboarding/complete', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ connectedAccounts }),
      });

      if (response.ok) {
        router.push('/dashboard');
      } else {
        const data = await response.json();
        setError(data.message || 'An error occurred while completing onboarding');
      }
    } catch (err) {
      setError('An error occurred. Please try again.');
    }
  };

  return (
    <div>
      <h2 className="text-2xl font-bold leading-7 text-gray-900">Connect Your Accounts</h2>
      <p className="mt-1 text-sm leading-6 text-gray-600">
        Connect your social media accounts to get started with GramChain.
      </p>

      {error && (
        <div className="mt-4 rounded-md bg-red-50 p-4">
          <div className="text-sm text-red-700">{error}</div>
        </div>
      )}

      <div className="mt-6 space-y-4">
        {socialPlatforms.map((platform) => (
          <div
            key={platform.id}
            className="relative flex items-center space-x-3 rounded-lg border border-gray-300 bg-white px-6 py-5 shadow-sm focus-within:ring-2 focus-within:ring-indigo-500 focus-within:ring-offset-2 hover:border-gray-400"
          >
            <div className="flex-shrink-0">{platform.icon}</div>
            <div className="min-w-0 flex-1">
              <a href="#" className="focus:outline-none">
                <span className="absolute inset-0" aria-hidden="true" />
                <p className="text-sm font-medium text-gray-900">{platform.name}</p>
                <p className="truncate text-sm text-gray-500">{platform.description}</p>
              </a>
            </div>
            <div className="flex-shrink-0">
              {connectedAccounts.includes(platform.id) ? (
                <span className="inline-flex items-center rounded-full bg-green-100 px-2.5 py-0.5 text-xs font-medium text-green-800">
                  Connected
                </span>
              ) : (
                <button
                  type="button"
                  onClick={() => handleConnect(platform.id)}
                  className="inline-flex items-center rounded-md bg-white px-2.5 py-1.5 text-sm font-semibold text-gray-900 shadow-sm ring-1 ring-inset ring-gray-300 hover:bg-gray-50"
                >
                  Connect
                </button>
              )}
            </div>
          </div>
        ))}
      </div>

      <div className="mt-6 flex justify-end gap-x-3">
        <button
          type="button"
          onClick={() => router.back()}
          className="rounded-md bg-white px-3 py-2 text-sm font-semibold text-gray-900 shadow-sm ring-1 ring-inset ring-gray-300 hover:bg-gray-50"
        >
          Back
        </button>
        <button
          type="button"
          onClick={handleFinish}
          disabled={connectedAccounts.length === 0}
          className="rounded-md bg-indigo-600 px-3 py-2 text-sm font-semibold text-white shadow-sm hover:bg-indigo-500 focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-600 disabled:opacity-50 disabled:cursor-not-allowed"
        >
          Finish Setup
        </button>
      </div>
    </div>
  );
} 